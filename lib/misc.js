
"use strict" ;



exports.fibonacciValue = v => {
	if ( v <= 0 ) { return 0 ; }
	if ( v <= 1 ) { return 1 ; }
	if ( v <= 2 ) { return 2 ; }
	if ( v <= 3 ) { return 3 ; }
	if ( v <= 5 ) { return 5 ; }
	if ( v <= 8 ) { return 8 ; }
	if ( v <= 13 ) { return 13 ; }
	if ( v <= 21 ) { return 21 ; }
	if ( v <= 34 ) { return 34 ; }
	if ( v <= 55 ) { return 55 ; }
	return 100 ;
} ;


